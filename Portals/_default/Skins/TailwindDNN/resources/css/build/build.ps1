# Tailwind v4 build script for DNN skins - zero config, project-agnostic.
#
# Drop this file into <skin>/resources/css/build/ and run it. That's it.
#
# What it does:
#   1. Bootstraps npm + tailwindcss on first run.
#   2. Auto-detects the DNN root by walking up until it finds DesktopModules/.
#      - If found: scans the entire skin AND DesktopModules/ for Tailwind classes.
#      - If not found (e.g. standalone skin checkout): scans the skin only.
#   3. Inlines every <style type="text/tailwindcss"> partial in resources/css/
#      (any file matching _*.html: _theme.html, _global.html, _UI.html, ...).
#   4. Compiles -> resources/css/tailwind.css (minified).

$ErrorActionPreference = 'Stop'
Set-Location -LiteralPath $PSScriptRoot

# --- Paths ----------------------------------------------------------------
$cssDir     = (Resolve-Path '..').Path                         # resources/css
$skinRoot   = (Resolve-Path '..\..\..').Path                   # the skin folder
$inputFile  = Join-Path $PSScriptRoot 'input.css'
$outputFile = Join-Path $cssDir 'tailwind.css'
$pkgFile    = Join-Path $PSScriptRoot 'package.json'
$nodeMods   = Join-Path $PSScriptRoot 'node_modules'

# --- 1) Bootstrap npm deps if missing -------------------------------------
if (-not (Test-Path -LiteralPath $pkgFile)) {
  Write-Host 'Initializing package.json...' -ForegroundColor Cyan
  & npm init -y | Out-Null
}
if (-not (Test-Path -LiteralPath $nodeMods)) {
  Write-Host 'Installing tailwindcss + @tailwindcss/cli (first run)...' -ForegroundColor Cyan
  & npm install --no-fund --no-audit -D tailwindcss '@tailwindcss/cli'
  if ($LASTEXITCODE -ne 0) { throw "npm install failed (exit $LASTEXITCODE)" }
}

# --- 2) Auto-detect DNN root (walk up looking for DesktopModules) ---------
function Find-DnnRoot([string]$start) {
  $dir = Get-Item -LiteralPath $start
  while ($null -ne $dir) {
    if (Test-Path -LiteralPath (Join-Path $dir.FullName 'DesktopModules')) {
      return $dir.FullName
    }
    if ($null -eq $dir.Parent) { return $null }
    $dir = $dir.Parent
  }
  return $null
}

$sources = New-Object System.Collections.Generic.List[string]
$skinGlob = ($skinRoot.Replace('\','/')) + '/**/*.{cshtml,ascx,html,js,css}'
$sources.Add($skinGlob) | Out-Null

$dnnRoot = Find-DnnRoot $skinRoot
if ($dnnRoot) {
  $modGlob = ($dnnRoot.Replace('\','/')) + '/DesktopModules/**/*.{cshtml,html,js}'
  $sources.Add($modGlob) | Out-Null
  Write-Host ('DNN root detected: {0}' -f $dnnRoot) -ForegroundColor DarkGray
} else {
  Write-Host 'No DNN root found - scanning skin only.' -ForegroundColor DarkGray
}

# --- 3) Inline every _*.html partial in cssDir ----------------------------
function Strip-StyleTags([string]$path) {
  if (-not (Test-Path -LiteralPath $path)) { return '' }
  $raw = Get-Content -LiteralPath $path -Raw
  return ($raw -replace '<style[^>]*>','' -replace '</style>','').Trim()
}

$partialFiles = Get-ChildItem -LiteralPath $cssDir -Filter '_*.html' -File -ErrorAction SilentlyContinue |
                Sort-Object Name
$partialBlocks = @()
foreach ($pf in $partialFiles) {
  $partialBlocks += "/* === $($pf.Name) === */`r`n" + (Strip-StyleTags $pf.FullName)
}
$partialNames = ($partialFiles | ForEach-Object { $_.Name }) -join ', '
if (-not $partialNames) { $partialNames = '(none found)' }

# --- 4) Assemble input.css ------------------------------------------------
Write-Host 'Generating input.css...' -ForegroundColor Cyan
$sourceLines = ($sources | ForEach-Object { '@source "' + $_ + '";' }) -join "`r`n"
$partialBody = ($partialBlocks -join "`r`n`r`n")

$content = @"
/* AUTO-GENERATED by build.ps1 - do not edit by hand. */
/* Inlined partials: $partialNames */
@import "tailwindcss";

$sourceLines

$partialBody
"@

Set-Content -LiteralPath $inputFile -Value $content -Encoding UTF8
Write-Host ('input.css written ({0:N0} bytes)' -f (Get-Item $inputFile).Length) -ForegroundColor DarkGray

# --- 5) Compile -----------------------------------------------------------
Write-Host 'Compiling Tailwind...' -ForegroundColor Cyan
& npx '@tailwindcss/cli' -i $inputFile -o $outputFile --minify
if ($LASTEXITCODE -ne 0) { throw "Tailwind CLI exited with code $LASTEXITCODE" }

$out = Get-Item $outputFile
Write-Host ('Done -> {0} ({1:N0} bytes)' -f $out.FullName, $out.Length) -ForegroundColor Green
